class Utils {

    static boolean isNumberEven( int value ) {
        ( value % 2 == 0 ) ? true : false
    }

    static boolean isMultipleOfThree( int value ) {
        ( value % 3 == 0 ) ? true : false
    }

    static boolean isMultipleOfFive( int value ) {
        ( value % 5 == 0 ) ? true : false
    }
}
